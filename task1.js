// Створюємо нове вікно розміром 300x300 пікселів
const newWindow = window.open('', '', 'width=300,height=300');

// Змінюємо розміри вікна після затримки у 2 секунди
setTimeout(() => {
  newWindow.resizeTo(500, 500);
}, 2000);

// Переміщуємо вікно в точку з координатами (200, 200) після затримки у 4 секунди (2 + 2)
setTimeout(() => {
  newWindow.moveTo(200, 200);
}, 4000);

// Закриваємо вікно після затримки у 6 секунд (2 + 2 + 2)
setTimeout(() => {
  newWindow.close();
}, 6000);
